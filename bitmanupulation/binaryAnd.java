package bitmanupulation;
import java.util.*;

public class binaryAnd {
    public static void main(String[] args) {
        System.out.println(5 & 6);
        // or
        System.out.println(5 | 6);
        // xor
        System.out.println(5 ^ 6);
        // not or compliment
        System.out.println(~5);
        // left shift
        System.out.println(5 << 2);
        // right
        System.out.println(6 >> 1);
    }
}
